	<h1 id="fairplay">Fairplay</h1>
	<h2 id="jouer-le-jeu">Jouer le jeu</h2>
	<p>
		Ce terme définit l’art de se mettre dans la peau d’un personnage, et d’agir comme lui le ferait,
		et non comme le joueur l’interprétant a l’habitude de le faire. Il concerne également la
		capacité de la joueuse à faire abstraction de ce que lui sait et que son personnage ignore.
		Enfin, il englobe toute la partie fair-play d’un GN, à savoir se laisser voler, tromper, tuer
		lorsque c’est le moment.
	</p>
	<h2 id="fairplay-et-confiance">Fairplay et confiance</h2>
	<p>
		On considère en GN que tout effet qui nous est expliqué par une Organ ou une autre PJ doit être
		tenu pour vrai et doit être appliqué sur le moment. Par exemple, si une PJ vient vers un autre
		et lui explique qu’elle a empoisonné sa boisson et qu’elle meurt sur-le-champ, et bien cette
		dernière doit mourir immédiatement, sans discuter, et faire semblant qu’il ne sait pas qui l’a
		empoisonné. Bien entendu, utiliser des compétences que son personnage ne maîtrise pas est
		considéré comme de la triche, et ce n’est vraiment pas bien vu dans l’univers du GN…
	</p>
	<h2 id="séparer-jeu-et-réalité">Séparer jeu et réalité</h2>
	<p>
		Il faut encore ajouter quelques règles plus “morales”; la principale est la suivante : tout ce
		qui se dit et se fait dans le jeu n’a pas d’influence sur la réalité, et tout ce qui s’est passé
		dans la réalité n’en a pas sur le jeu. Il est très important de distinguer ces deux temps. Deux
		personnages qui se détestent ne sont pas incarnés par des ennemis jurés ; leur rôle leur impose
		cette relation, c’est tout. Un personnage se faisant insulter en jeu ne doit pas prendre ces
		mots personnellement contre lui, puisque c’est son PERSONNAGE qui est insulté. Mais attention !
		Il est intolérable de se servir du GN pour se défouler sur les personnes que l’on n’apprécie pas
		! Le jeu ne doit EN AUCUN CAS être utilisé de cette manière ! A nouveau, en cas de problème avec
		d’autres joueurs, il est possible d’avertir les Orgas.
	</p>

	<h1 id="sécurité-physique">Sécurité physique</h1>
	<h2 id="la-sécurité-en-priorité">La sécurité en priorité</h2>
	<p>
		Nous jouons ensemble. Il s’agit d’un JEU. Toute situation mettant en danger quelqu’un doit être
		évitée, et doit mener à un bref arrêt du jeu. Par exemple, si deux personnes combattent trop
		près d’un ravin, elles ne doivent pas hésiter à faire une pause dans le jeu pour se déplacer et
		poursuivre leur combat dans un lieu plus sécurisé. De même, il est dangereux de mettre sa propre
		santé en danger afin de préserver celle de son personnage… En cas de problème, les Orgas sont
		présentes, et il est bon de les avertir, car iels ont souvent des solutions à proposer (d’ordre
		médical, logistique, etc.).
	</p>
	<h2 id="les-armes-de-jeu">Les armes de jeu</h2>
	<p>
		Les combats en jeu se font à l’aide d’armes spécialement créées pour le GN. Toutes les armes
		sont vérifiées avant le jeu par les Orgas du GN. Les armes doivent être conçues en mousse (ou
		matériaux similaires) pour être acceptées. Celles qui contiennent une fibre de verre ou de
		carbone ne peuvent en aucun cas être lancées, et aucun coup d’estoc ne peut être porté avec.
	</p>
	<h2 id="les-attaques">Les attaques</h2>
	<p>
		TOUS LES COUPS DOIVENT ÊTRE RETENUS ! Même une arme de GN fait mal lorsque l’on utilise beaucoup
		de force pour frapper avec. Si une PJ ne respecte pas ces règles de sécurité, il est possible
		d’en avertir les Organisateurs, qui calmeront son bras trop puissant.
	</p>
	<h2 id="respecter-le-périmètre">Respecter le périmètre</h2>
	<p>
		Au début de chaque GN, les Orgas définissent un périmètre de jeu; ces limites ne doivent être
		dépassées en aucun cas. Aucune scène du jeu n’aura lieu en dehors de cette zone, rien n’y sera
		caché, donc il n’y a absolument rien à y faire. La plupart du temps, l’aire de jeu est limitée
		pour des raisons de sécurité ou de légalité (il serait ennuyeux pour une voisine de voir quinze
		GNistes dans son jardin un dimanche matin…). De manière générale, lorsque les Orgas donnent des
		instructions, elles ont lieu d’être et doivent être respectées, que ce soit avant, après ou
		durant le GN. C’est particulièrement pertinent cette année car une partie du terrain (caché,
		nous serons en intimité) sera utilisée par les scouts.
	</p>
	<h2 id="le-matériel">Le matériel</h2>
	<p>
		Enfin, le respect des autres s’étend également à leur matériel. Les armes, accessoires et
		costumes de GN sont chers à acheter, et longs à créer; il est normal d’y faire attention, et
		d’en prendre soin. Typiquement, lorsque des armes traînent par terre sur un champ de bataille
		(flèches, le plus souvent), il est bon de les écarter rapidement des pieds des barbares, si
		c’est possible.
	</p>

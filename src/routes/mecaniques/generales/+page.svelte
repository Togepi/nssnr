	<h1 id="règle-du-jeu-générales">Règles de jeu générales</h1>
	<h2>Pour commencer</h2>
	<p>
		En jeu se trouve un personnage particulier, la Marchande. Ce personnage gère du commerce, répond
		à toutes les questions en cours de jeu et assure le suivi des différentes mécaniques de jeu et
		systèmes de simulation.
	</p>
	<p>
		Pour assurer un respect du matériel de chaque PJ, il est INTERDIT de voler les armes ou
		accessoires des autres PJ. Les gros objets volables sont marqués d&#39;une étiquette par les
		Orgas (cette étiquette peut être déplacée d&#39;une arme à l&#39;autre au besoin); les petits se
		trouvent dans les poches, sacs, etc. et peuvent être fouillés ou volés par larcin, toujours avec
		l&#39;assistance de la “ victime propriétaire ”. Le reste ne doit pas être touché ou pris.
	</p>
	<h2 id="compétence">Compétence</h2>
	<p>
		Pour pouvoir manier une arme ou un bouclier, il faut nécessairement avoir acheté la compétence
		correspondante. Pour pouvoir tenter de crocheter un coffre, il faut posséder &quot;Paria&quot;.
	</p>
	<p>
		Tout coffre ou porte couvert d’un bout de scotch avec un pictogramme de clef est comme fermé par
		une serrure et ne peut être retiré que si une personne avec la capacité “saboteuse” passe 3min à
		l’ouvrir. Les coffres peuvent être piégés. Un pétard explosant fait 1 dégât sans armure (cf.
		Système de santé). Une araignée (en plastique) donne une altération d&#39;état au toucher;
		l&#39;araignée est posée dans un coffre, une bourse, une poche, etc. et celui qui la touche
		subit l&#39;altération d&#39;état indiquée dessus immédiatement après le contact (cf.
		Altérations d&#39;état).
	</p>
	<p>
		Les drogues, champignons et autres médecines possèdent un petit papier expliquatif. Ce papier ne
		peut être lu qu&#39;après avoir consommé l’objet, et l&#39;effet doit être appliqué sur-le-
		champ. Une drogue utilisée retourne en salle Orga. Les drogues font effet pendant 10 Sabliers
		(~30mn) au maximum, à moins que l&#39;effet ne spécifie autre chose. Si vous comptez consommer
		plusieurs drogues à la suite et cumuler leurs effets, nous vous encourageons à prendre en compte
		la possibilité d’une overdose.
	</p>
	<p>
		Le temps des effets se compte souvent en Sabliers: un Sablier vaut 3 minutes, 3 Sabliers en
		valent 10.
	</p>
	<p>
		Les Parias peuvent poignarder, en arrivant discrètement derrière leur victime. Iels doivent
		poser la main sur son épaule et lui faire une croix dans le dos à l&#39;aide d&#39;une dague. La
		victime passe alors immédiatement à 0 PV (donc tombe à l’agonie la plupart du temps), et doit
		tomber au sol en suivant les mêmes règles que pour un &quot;Assomme!&quot; (voir plus loin,
		Mots-clés).
	</p>
	<p>
		Une main tendue sur la tête représente une invisibilité en jeu. Un poing fermé sur la tête ou un
		masque / ruban / maquillage présenté au briefing représente un statut hors-jeu: aucun personnage
		n&#39;existe, ce que l&#39;on vient de voir est un Organisateur ou une PJ dont le personnage a
		totalement disparu.
	</p>
	<p>
		Toutes les actions propres aux compétences d&#39;artisanat prennent du temps. De manière
		générale, il est nécessaire de travailler durant deux Sabliers (5 minutes) pour chaque action de
		ce type (créer ou réparer un objet, soigner, etc.).
	</p>
	<p>
		Les Parias sont muni de pinces à linges qu’iels peuvent placer sur les personnes qu’iels
		souhaitent voler. Si la Paria a placé trois pinces à linge sur les vêtements d’une personne sans
		se faire prendre héritera de la bourse de cette dernière. Lorsqu’une PJ remarque qu’elle a trois
		pinces sur lui, elle se rend vers le marchand pour y laisser le contenu de sa bourse ainsi que
		les pincettes. La Paria peut passer récupérer les fruits de son larcin plus tard.
	</p>
	<h2 id="la-monnaie">La monnaie</h2>
	<ul>
		<li>
			Les Unités (<strong>U</strong>): Rondelles de fer percée. C’est la monnaie la plus courante
			dans la région.
		</li>
	</ul>
	<h2>Système de santé</h2>

	<p>
		Chaque personnage ne possède qu'un unique Point de Vie (PV), à moins qu'un effet particulier ne
		lui soit appliqué.
	</p>

	<p>Quand un personnage qui n’a qu’un point de vie se fait toucher :</p>

	<ul>
		<li>Dans le torse : Il agonise.</li>
		<li>
			Dans le bras ou à la jambe : le membre est fracturé et ne peut plus être utilisé.
			<ul>
				<li>Si le membre fracturé est touché, il agonise.</li>
				<li>Tous les membres peuvent être fracturés sans que le personnage agonise.</li>
			</ul>
		</li>
		<li>Dans la tête (ne pas viser la tête mais une balle perdue, ça arrive) : Il agonise.</li>
	</ul>

	<p>
		<strong>Agonie :</strong> lorsqu’un personnage entre en agonie, il est sonné ou inconscient pendant
		3min. Puis il peut se relever pour aller chercher de l’aide. Il ne peut ni courir, ni se battre efficacement.
		Il ne peut plus se servir de ses compétences (y compris raciale) et connaissances. Le reste est laissé
		à son libre jeu (entrave pour marcher, courir, parler, douleur constante, etc.).
	</p>

	<p>
		Soigner l’agonie nécessite de ramener le personnage à un PV. La manière la plus rapide est de
		lui donner un médicament (champignon ou quickmed).
	</p>

	<p>
		Lorsqu’un personnage agonisant se fait toucher, il tombe dans le coma. Un personnage dans le
		coma.
	</p>

	<p>
		Un personnage qui tombe en agonie peut également décider après avoir passé 3 min inconscient de
		se relever pour faire un "Héroïsme". Le personnage peut choisir une action qui souhaite
		effectuer (emmener son ami hors de portée des attaquants, guérir un médecin compétent, détruire
		un objet, abattre un ennemi, etc.) et tombe dans le coma quand :
	</p>

	<ul>
		<li>l’action est effectuée.</li>
		<li>l’action ne peut plus être effectuée.</li>
		<li>il prend un coup.</li>
	</ul>

	<p>
		La durée de l'action choisie pour l'héroïsme doit pouvoir s'effectuer dans les 5 minutes suivant
		son annonce.
	</p>

	<p>
		Un personnage dans le coma est inconscient. Si personne ne vient le sauver dans les 10min, il
		meurt définitivement. Le joueur revient en Salle Orga.
	</p>

	<p>
		Un personnage qui a subi un cannibalisme est retiré du jeu. Attention, les organisateurs ne
		laisseront peut-être pas un personnage cannibalisé dans les premières heures du GN mourir si
		facilement...
	</p>

	<p>
		Chaque coup physique reçu inflige initialement 1 dégât. L'annonce d'une valeur au moment où le
		coup est porté inflige autant de dégâts. Par exemple, en annonçant “3 !”, la victime subit 3
		dégâts. Les dégâts sont encaissés par l'armure que la victime porte, jusqu'à ce qu'elle se
		brise. Puis, ce sont ses PV qui encaissent les dégâts. Lorsqu'un personnage n'a qu'un seul PV
		(situation normale), on ne compte plus le nombre de dégâts infligés, mais le nombre de coups
		portés. Si un personnage possède 2 PV, le premier coup l'amènera à 1 PV sans le blesser de
		manière significative.
	</p>

	<p>
		Les points d’armures ne peuvent pas tomber dans les négatifs mais encaissent l’excédent de
		dégâts. Un personnage qui prend 5 dégâts et qui n’a que deux points d’armure et 1PV n’entre pas
		instantanément en agonie.
	</p>

	<p>
		Sans point d'armure (ou suite à un coup annoncé “sans-armure !”), après avoir subi un coup sur
		un membre (bras ou jambes), le personnage passe en fracture : il peut être soigné par un
		médecin.
	</p>

	<p>
		Chaque joueur est libre de jouer les dégâts reçus comme il le souhaite ; la douleur et
		l'intensité de la blessure est choisie par le joueur pour tout ce qui concerne le jeu. En
		revanche, la mécanique de jeu suit les règles présentées ci-dessus (système de blessures, de
		mort, etc.).
	</p>
	<h2>Armes à feu</h2>

	<p>
		Les armes à feu autorisées durant le GN sont des pistolets et fusils de la marque NERF ou
		similaire qui prennent les mêmes darts en mousse.
	</p>

	<ul>
		<li>Les pistolets se tiennent à une main et ont un max de 6 munitions dans le chargeur.</li>
		<li>
			Les fusils se tiennent à deux mains et n’ont pas de limite de munition dans le chargeur.
		</li>
		<li>
			Les armes NERF dotées d’un moteur électrique et qui tirent automatiquement sont considérées
			comme des “armes automatiques”.
		</li>
	</ul>

	<h2>Armes à feu lourdes et BOOM !</h2>

	<p>
		Les armes à feu lourdes ou armes lourdes sont les lance-flammes ou les lance-roquettes. Ces
		armes n’ont pas besoin d’avoir un mécanisme de projection et sont simplement utilisées par le
		joueur en criant “BOOM” ou “PRRRRRRRR” (Bruit du lance-flamme). Il est également possible pour
		un joueur d’apporter une sulfateuse qui ferait “RATATATATA”, du moment qu’on comprend qu’une
		arme lourde est utilisée.
	</p>

	<p>
		Lorsqu’une arme lourde ou qui fait BOOM est utilisée, tous les personnages dans un cône de 5m
		subissent 2 dégâts sans armure et leurs armures sont détruites. Quand une arme lourde ou un
		BOOM! est utilisé dans une pièce restreinte (petite chambre, couloir) l’attaque touche toutes
		les personnes présentes (attention !).
	</p>

	<p>
		Les munitions d’armes lourdes sont notées sur l’arme par un scotch. Une arme lourde avec deux
		bouts de scotch sur elle peut tirer deux fois avant d’être à court de munitions.
	</p>

	<ul>
		<li>Les munitions d’armes lourdes sont très rares, attention !</li>
		<li>Les grenades font BOOM! et sont une alternative moins coûteuse aux armes lourdes.</li>
	</ul>

	<h2>Se rendre sur le lieu</h2>
	<ul>
		<li><strong>Adresse :</strong> Route d’Air-la-ville 153, 1242 Satigny, Genève</li>
		<li>
			Les personnes qui souhaitent dormir sur place le vendredi 30 octobre : il n’y aura pas de
			repas du soir ni de petit déjeuner pour les joueuses le lendemain matin (prévoir quelque
			chose). Les PNJ ont un repas le soir et un petit déjeuner prévus.
		</li>
		<li><strong>PJ :</strong> rendez-vous le 31 octobre à 10h sur le lieu.</li>
		<li><strong>Fin du GN :</strong> dimanche 1er novembre vers 12h–14h.</li>
		<li>Il y a un parking à l’entrée.</li>
		<li>Nous pouvons organiser des navettes depuis la gare de Satigny.</li>
	</ul>

	<h2>Manger</h2>
	<ul>
		<li>Il n’y aura rien vendredi soir pour celles qui viennent : apportez votre pique-nique.</li>
		<li>Le samedi matin et vendredi soir, il y aura à manger pour les PNJ/Bénévoles.</li>
		<li>
			Quatre repas en jeu : samedi midi, samedi soir, dimanche matin et dimanche midi. Plus des
			collations.
		</li>
		<li>Nous prendrons en compte vos allergies/régimes. Les repas seront vegan de base.</li>
	</ul>

	<h2>Dormir</h2>
	<ul>
		<li>
			Vous aurez des lits mais il faudra prendre un sac de couchage (et un coussin si vous le
			souhaitez).
		</li>
		<li>Il est possible de faire du camping sur le terrain, mais il fera probablement froid.</li>
		<li>Le jeu s’arrête complètement de 00h à 08h pour que tout le monde puisse se reposer.</li>
	</ul>

	<h2>Respect du Domaine</h2>
	<ul>
		<li>De manière générale : respectez le lieu (bon sens).</li>
		<li>Nous ferons attention à la propreté afin de gagner du temps pour le rangement.</li>
		<li>
			Pas de chaussures d’extérieur à l’intérieur. Avec des rangers, vous perdrez 2 minutes à chaque
			entrée/sortie.
		</li>
		<li>Ne pas faire de feux hors des espaces prévus.</li>
		<li>Nous rendons le lieu à 17h le dimanche.</li>
	</ul>

	<h2>Parents et enfants</h2>
	<p>
		Il est possible d’amener des enfants en jeu. Cependant, nous souhaitons d’abord discuter avec
		vous pour vérifier si l’activité est adaptée, afin de ne pas péjorer les joueurs adultes.
	</p>
	<p>
		Les adolescents sont les bienvenus, mais doivent trouver un adulte présent sur place qui peut
		signer leur décharge (fiche d’inscription). Ils peuvent demander sur le Discord ou aux
		organisatrices.
	</p>


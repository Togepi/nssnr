	<h2>À prendre le jour J</h2>
	<ul>
		<li>Sac de couchage (les lits n’ont pas de couvertures) + housse de lit + taie d’oreiller.</li>
		<li>Des habits chauds et de rechange.</li>
		<li>Des pantoufles ou chaussures d’intérieur/gym, ou de grosses chaussettes.</li>
		<li>Votre matériel de toilette si vous souhaitez vous doucher.</li>
		<li>Lampe de poche/frontale : le soleil se couche vers 17h.</li>
		<li>Votre costume et vos armes. Matériel de camping si besoin.</li>
		<li>Une copie de votre rôle de personnage.</li>
		<li>Votre bonne humeur.</li>
	</ul>

	<h1>Créer son personnage</h1>
	<h2 id="0-regardez-un-résumé-de-lunivers">0. Regardez un résumé de l’univers</h2>
	<p>
		Ce document de création ne répète pas ce qui est déjà fourni dans les résumés de l’univers (PDF
		et Vidéo), je vous invite à les lire pour plus de clarté. Cependant, comme l’univers prend ses
		racines dans un jeu de rôle sur papier, faites attention de ne pas en retenir les règles, car le
		GN n’en aura bien évidemment pas les mêmes.
	</p>
	<h2 id="1-choisir-une-espèce">1. Choisir une espèce</h2>
	<p>
		Il existe vingt espèces dans NSSN mais, dans ce GN, deux d&#39;entre-elles seront mises de côté
		pour des raisons de praticité. Ce sont les Centaures (qui nécessiteraient un costume
		d’homme-cheval) et les Fidgets (qui font 30cm et qui ont une apparence bien particulière). Cela
		dit, si vous êtes particulièrement motivés à faire un costume de centaure ou de fidget pour
		l’occasion, il est bien possible que votre personnage soit accepté en jeu.
	</p>
	<h2 id="2-choisir-un-job">2. Choisir un job</h2>
	<p>
		Le “job” de votre personnage désigne son occupation et sa classe sociale dans le monde. Les jobs
		vous donneront des points d&#39;équipements dit “PE”, pour acheter votre matériel de base dans
		la liste d&#39;équipement. Attention, certains équipements nécessitent des compétences pour être
		utilisés.
	</p>
	<h2 id="3-choisir-ses-compétences">3. Choisir ses compétences</h2>
	<p>
		Vous trouverez une liste de compétences pour donner certaines capacités à votre personnage.
		Chaque personnage commence avec dix points de compétences (10 PC), sauf les humaines qui en ont
		onze (11 PC).
	</p>
	<h2 id="4-acheter-son-matériel-de-départ">4. Acheter son matériel de départ</h2>
	<p>
		Grâce à votre job, vous aurez un certains nombres de points d’équipement (PE) à dépenser pour
		acheter des armes, armure, drogue, trésors et autres ressources pour votre personnage.
	</p>
	<h2 id="5-écrire-son-histoire-background-bg">5. Écrire son histoire (background (BG))</h2>
	<p>
		Le BG est le passé de votre personnage, ainsi que son caractère et ses éventuels objectifs de
		vie. Cette étape consiste à modeler &quot;l&#39;intérieur&quot; de votre personnage. Commencez
		par lui donner un nom (éventuellement plusieurs). Il faut savoir que même si le BG est libre,
		s&#39;il ne se marie pas à l&#39;univers, il ne sera pas accepté par les Orgas (un Luke
		Skywalker n&#39;aura pas sa place dans cet univers de jeu). Un autre point important est que le
		BG importe beaucoup, notamment pour expliquer tel ou tel choix de compétences ou de matériel.
		Les Orgas aiment la cohérence dans les personnages, pensez-y! Il n&#39;est pas nécessaire
		d&#39;écrire un roman, mais évitez juste la phrase unique... Ce BG vous permettra d&#39;entrer
		dans un personnage concret durant le jeu.
	</p>
	<h2 id="6-faire-partie-dun-groupe-ou-dune-famille-facultatif">
		6. Faire partie d&#39;un groupe ou d’une famille (facultatif)
	</h2>
	<p>
		Vous avez la possibilité de venir en groupe aux GNs, si vous souhaitez jouer avec vos amis. Il
		n&#39;y a aucune limite fixe au nombre de personnes dans un groupe, mais les Orgas se réservent
		le droit de refuser un trop grand groupe pour des raisons pratiques (ou autres). Notez dans le
		formulaire le nom de votre groupe, ainsi que le nom des autres membres de ce groupe, si
		possible.
	</p>
	<h2 id="7-effectuer-le-paiement">7. Effectuer le paiement</h2>
	<p>
		Pour que votre personnage soit traité par les Orgas, il est nécessaire que le paiement ait été
		effectué. Référez-vous à la page d’inscription pour les informations relatives au paiement.
	</p>

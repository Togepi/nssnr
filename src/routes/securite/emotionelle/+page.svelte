	<h1 id="sécurité-émotionnelle">Sécurité émotionnelle</h1>
	<h2 id="prévenir-plutôt-que-guérir">Prévenir plutôt que guérir</h2>
	<p>
		Merci de bien indiquer si un sujet vous est particulièrement sensible. Nous prenons en compte
		les besoins émotionnels de chaque personne et adaptons au maximum l’univers et le scénario en
		fonction de ceux-ci. Si, malgré toutes les précautions prises, la personne se sent
		inconfortable, merci de prévenir l’équipe le plus rapidement possible afin que nous puissions
		prendre les dispositions nécessaires.
	</p>
	<h2 id="voici-les-thèmes-qui-sont-abordés-lors-de-ce-we-">
		Voici les thèmes qui peuvent être abordés lors de ce WE :
	</h2>
	<ul>
		<li>Violence et agression (pouvant mener à la mort)</li>
		<li>Visuel gore (sang, organe)</li>
		<li>Auto-mutilation</li>
		<li>Maladie léthale</li>
		<li>Famine</li>
		<li>Torture</li>
		<li>Génocide</li>
		<li>Paralysie / claustrophobie</li>
		<li>Mort-vivant</li>
		<li>Suicide</li>
		<li>Trouble psychique (schizophrénie, paranoïa,…)</li>
		<li>Désastre naturel</li>
		<li>Violence spéciste</li>
		<li>Nudité</li>
		<li>Sonorité forte (explosion, alarme)</li>
		<li>Lumière stroboscopique</li>
	</ul>
	<h2 id="en-fonction-des-demandes">En fonction des demandes</h2>
	<p>
		En fonction des demandes, ces thèmes peuvent potentiellement disparaître. Gardez en tête que
		l’idée de ce Gn est de se rapprocher du drame et que ces thématiques ne seront pas abordées sous
		un abord superficiel. Pour que nous puissions vous garantir une richesse émotionnelle lors de ce
		WE, merci de communiquer le plus tôt possible si une thématique vous intéresse ou vous repousse.
		Nous mettons le maximum de mesures pour prévenir tout inconfort, mais la communication reste le
		vecteur principal de toute sécurité.
	</p>
	<h2 id="safe-zone">Safe zone</h2>
	<p>
		Une salle spéciale sera dédiée au repos et sera considérée comme hors-jeu tout le we. Nous vous
		indiquerons la salle pendant la présentation du jeu. Ce n’est pas une salle de détente mais de
		repos. Les personnes qui ressentent la nécessité de l’utiliser doivent pouvoir le faire en y
		retrouvant de la quiétude. Pour un espace détente, nous vous invitons à vous poser à la taverne.
	</p>
	<h2 id="safeword-et-safegeste">Safeword et safegeste</h2>
	<p>
		Le concept du safeword permet d’exprimer son inconfort. Une fois qu’il est exprimé, les
		personnes autour doivent s’arrêter de jouer. Le jeu ne reprend qu’une fois assuré du bien-être
		de la personne. Le safeword est donc “vraiment vraiment”. Si une personne dit qu’elle est
		“vraiment vraiment gênée”, alors l’allocutaire doit considérer que c’est la PJ et non le
		personnage qui s’exprime, et prend les dispositions nécessaires. Le safegeste est le même
		concept mais appliqué à un geste, à savoir aux deux bras qui se croisent.
	</p>
